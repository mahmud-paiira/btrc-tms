    # ── Report 14: Revenue (Fee Collection) ──────────────────────────────

    def revenue(self):
        q = Q()
        circulars = Circular.objects.filter(q).exclude(fee__isnull=True).exclude(fee=0)
        total_fee = circulars.aggregate(s=Sum('fee'))['s'] or 0
        total_seats = circulars.aggregate(s=Sum('total_seats'))['s'] or 0
        filled_seats = circulars.aggregate(s=Sum(
            F('total_seats') - F('remaining_seats'),
        ))['s'] or 0
        est_revenue = total_fee * filled_seats
        rows = [
            ['Total Circulars with Fees', circulars.count()],
            ['Total Seats Offered', total_seats],
            ['Filled Seats', filled_seats],
            ['Fee per Seat (Avg)', f'৳{round(total_fee / circulars.count() if circulars.count() else 0, 0)}'],
            ['Estimated Revenue', f'৳{est_revenue}'],
        ]
        return {
            'title': 'Revenue Report',
            'headers': ['Metric', 'Value'],
            'rows': rows,
        }

    # ── Report 15: Gender Diversity ──────────────────────────────────────

    def gender_diversity(self):
        centers = Center.objects.filter(status='active')
        rows = []
        grand_male = grand_female = 0
        for c in centers:
            trainees = Trainee.objects.filter(center=c)
            male = female = 0
            for t in trainees:
                try:
                    g = t.user.profile.gender
                    if g == 'male': male += 1
                    elif g == 'female': female += 1
                except UserProfile.DoesNotExist:
                    pass
            total = male + female
            f_pct = round(female / total * 100, 1) if total else 0
            grand_male += male
            grand_female += female
            rows.append([c.code, c.name_bn, total, male, female, f'{f_pct}%'])
        gt = grand_male + grand_female
        rows.append(['TOTAL', '', gt, grand_male, grand_female, f'{round(grand_female/gt*100, 1) if gt else 0}%'])
        return {
            'title': 'Gender Diversity Report',
            'headers': ['Code', 'Center', 'Total', 'Men', 'Women', 'Female %'],
            'rows': rows,
        }

